const searchInput = document.querySelector('.search-box input');
const searchDropdown = document.querySelector('.search-dropdown');

const searchData = (searchValue) => {
    const searchResults = data.filter((search) => {
        return search.name.toLowerCase().includes(searchValue.toLowerCase());
    });

    if (searchResults.length) {
        searchDropdown.innerHTML = '';
        searchResults.forEach((search) => {
            const searchItem = document.createElement('li');
            searchItem.classList.add('search-item');
            searchItem.innerHTML = search.html;
            searchDropdown.appendChild(searchItem);
        });
    } else {
        searchDropdown.innerHTML = '<div class="search-error">No results found</div>';
    }
};

const searchHandler = () => {
    const searchValue = searchInput.value;
    if (!searchValue) return searchDropdown.classList.add('hidden');

    searchData(searchValue);
    searchDropdown.classList.remove('hidden');
};

searchInput.addEventListener('input', searchHandler);
